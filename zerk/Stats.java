package zerk;

public class Stats {
	public int str;
	public int con;
	public int dex;
	public int spd;
	public int mag;
	public int att;
	
	public Stats(){
	}
	
	public Stats(int str, int con, int dex, int spd, int mag, int att){
		this.str = str;
		this.con = con;
		this.dex = dex;
		this.spd = spd;
		this.mag = mag;
		this.att = att;
	}
}